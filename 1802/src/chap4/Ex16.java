package chap4;

public class Ex16 {
	
	int kor, eng, mat, com, his, sum;
	double avg; // 평균
	
	
	
	
	public Ex16() {
		kor = 90; // 국어 점수
		eng = 11; // 영어 점수
		mat = 77; // 수학 점수
		com = 56; // 전산 점수
		his = 83; // 역사 점수
		sum = kor + eng + mat + com + his; // 총점
		avg = sum / 5.0;
	}
	
	public void compute() {
				
		if ((avg >= 60) && (kor >= 40) && (eng >= 40) && (mat >= 40) && (com>=40) && (his>=40)) {
			/* 모든 과목 >= 40 대신 , 모든 과목 < 40 으로, ||으로 연산 가능*/
			System.out.println("합격");			
		} else {
			System.out.println("불합격");
		}
	}

}
