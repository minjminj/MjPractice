// 단, 각 점수 중 40미만이 있는 경우는 평균이 60이상 이더라도 불합격.
// 작성일자: 2018.04.18
// 필드, 생성자, 매소드로 구분. 필드: 모든변수들. 생성자: 총점,평균 계산. 메소드: compute() -> 비고 및 출력 2018.04.19
