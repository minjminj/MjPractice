package chap4;

public class Ex13 {

	String name = "김민정"; // 이름

	double W = 86.0; // 체중
	double H = 164.0; // 키

	public Ex13() {
		
		double fat; // 비만율
		
		fat = H - 110;
		
		if (W >= fat) {				// 몸무게를 기준으로 기준값과 비교하는 것이 더 읽기 편하다
			System.out.println(name + "님은 과체중입니다");
	}

	
	}
}
